var daysUntilMyBirthday = 60;

while( daysUntilMyBirthday > 0 )
{
  if( daysUntilMyBirthday < 5 )
  {
    console.log( daysUntilMyBirthday + " DAYS UNTIL MY BIRTHDAY....OMG!!!")
  }
  else if( daysUntilMyBirthday < 30)
  {
    console.log( daysUntilMyBirthday + " days until my birthday....So Excited!!!")
  }
  else if( daysUntilMyBirthday >= 30 )
  {
    console.log( daysUntilMyBirthday + " days until my birthday....It's been forever T-T")
  }

  daysUntilMyBirthday--;
}

console.log("It's My Birthday!!! Happy Birthday to me :)")
